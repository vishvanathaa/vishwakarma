    getEntryTime: function () {
      var currentDate = new Date();
      currentDate.setDate(currentDate.getDate());
      var month = new Array();
      month[0] = "January";
      month[1] = "February";
      month[2] = "March";
      month[3] = "April";
      month[4] = "May";
      month[5] = "June";
      month[6] = "July";
      month[7] = "August";
      month[8] = "September";
      month[9] = "October";
      month[10] = "November";
      month[11] = "December";
      var day = new Array();
      day[0] = "Sunday";
      day[1] = "Monday";
      day[2] = "Tuesday";
      day[3] = "Wednesday";
      day[4] = "Thursday";
      day[5] = "Friday";
      day[6] = "Saturday";
      var monName = month[currentDate.getMonth()];
      var currentDay = day[currentDate.getDay()];
      var currentDateWithFormat =
        currentDay +
        ", " +
        monName +
        " " +
        currentDate.getDate() +
        ", " +
        currentDate.getFullYear();
      return currentDateWithFormat;
    },
    fetchdata() {
      db.collection("news")
        //.where("id", "==", "ghlML9lrwpujjCcBwKCw")
